void	print_stack(t_stacks *stack)
{
	int i;

	if (stack->a_top > stack->b_top)
		i = stack->a_top;
	else
		i = stack->b_top;
	while (i >= 0)
	{
		if (i <= stack->a_top)
			printf("| %d |", stack->a[i]);
		else
			printf("|   |");
		if (i <= stack->b_top)
			printf("| %d |", stack->b[i]);
		else
			printf("|   |");
		printf("\n");
		i--;
	}
}

